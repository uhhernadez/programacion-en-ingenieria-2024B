// Operación módulo
#include <iostream>
using namespace std;

int main () {
  cout << " 9 % 8 = " << 9 % 8 << endl;
  cout << " 6 % 8 = " << 6 % 8 << endl;

  cout << " 0 % 3 = " << 0 % 3 << endl;
  cout << " 1 % 3 = " << 1 % 3 << endl;
  cout << " 2 % 3 = " << 2 % 3 << endl;
  cout << " 3 % 3 = " << 3 % 3 << endl;
  cout << " 4 % 3 = " << 4 % 3 << endl;
  cout << " 5 % 3 = " << 5 % 3 << endl;
}